package net.bitacademy.java41.vo;

public class Mileage {
	private int sno;
	private int cash_mileAge_rate;
	private int card_mileAge_rate;
	
	
	public int getSno() {
		return sno;
	}
	public Mileage setSno(int sno) {
		this.sno = sno;
		return this;
	}
	public int getCash_mileAge_rate() {
		return cash_mileAge_rate;
	}
	public Mileage setCash_mileAge_rate(int cash_mileAge_rate) {
		this.cash_mileAge_rate = cash_mileAge_rate;
		return this;
	}
	public int getCard_mileAge_rate() {
		return card_mileAge_rate;
	}
	public Mileage setCard_mileAge_rate(int card_mileAge_rate) {
		this.card_mileAge_rate = card_mileAge_rate;
		return this;
	}
	
	
	
	
	
}

package net.bitacademy.java41.vo;

public class Stamp {
	private int stampno;
	private int sno;
	private int stamp_rate;
	private String stamp_content;
	private int stamp_level;
	private String stamp_start;
	private String stamp_end;
	
	public String getStamp_start() {
		return stamp_start;
	}
	public Stamp setStamp_start(String stamp_start) {
		this.stamp_start = stamp_start;
		return this;
	}
	public String getStamp_end() {
		return stamp_end;
	}
	public Stamp setStamp_end(String stamp_end) {
		this.stamp_end = stamp_end;
		return this;
	}
	public int getStampno() {
		return stampno;
	}
	public Stamp setStampno(int stampno) {
		this.stampno = stampno;
		return this;
	}
	public int getSno() {
		return sno;
	}
	public Stamp setSno(int sno) {
		this.sno = sno;
		return this;
	}
	public int getStamp_rate() {
		return stamp_rate;
	}
	public Stamp setStamp_rate(int stamp_rate) {
		this.stamp_rate = stamp_rate;
		return this;
	}
	public String getStamp_content() {
		return stamp_content;
	}
	public Stamp setStamp_content(String stamp_content) {
		this.stamp_content = stamp_content;
		return this;
	}
	public int getStamp_level() {
		return stamp_level;
	}
	public Stamp setStamp_level(int stamp_level) {
		this.stamp_level = stamp_level;
		return this;
	}
	
	
	
	
	
}
